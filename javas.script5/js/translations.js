// Language management class
class LanguageManager {
    constructor() {
        this.currentLanguage = localStorage.getItem('language') || 'uk';
        this.init();
    }

    init() {
        this.updateLanguageSelector();
        this.translatePage();
    }

    setLanguage(language) {
        this.currentLanguage = language;
        localStorage.setItem('language', language);
        this.updateLanguageSelector();
        this.translatePage();
    }

    getText(key, params = {}) {
        let text = translations[this.currentLanguage][key] || 
                   translations['uk'][key] || 
                   key;
        
        // Replace parameters
        Object.keys(params).forEach(param => {
            text = text.replace(`{${param}}`, params[param]);
        });
        
        return text;
    }

    updateLanguageSelector() {
        const selector = document.getElementById('languageSelector');
        if (selector) {
            selector.value = this.currentLanguage;
        }
    }

    translatePage() {
        // Translate all elements with data-translate attribute
        document.querySelectorAll('[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            const translatedText = this.getText(key);
            
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translatedText;
            } else {
                element.textContent = translatedText;
            }
        });

        // Update page title
        const titleKey = document.querySelector('title')?.getAttribute('data-translate');
        if (titleKey) {
            document.title = this.getText(titleKey);
        }
    }
}

// Initialize language manager
window.languageManager = new LanguageManager();

// Function to change language
function changeLanguage(language) {
    languageManager.setLanguage(language);
} 